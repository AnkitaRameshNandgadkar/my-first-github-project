# Python Data Types Example

# Integer
age = 21
print("Age:", age)

# Float
height = 5.6
print("Height:", height)

# String
name = "Ankita"
print("Name:", name)

# Boolean
is_student = True
print("Is Student:", is_student)

# List
subjects = ["Python", "AI", "ML"]
print("Subjects:", subjects)

# Dictionary
student = {
    "name": "Ankita",
    "age": 21,
    "course": "Computer Science"
}
print("Student Details:", student)
